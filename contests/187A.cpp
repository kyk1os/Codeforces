using namespace std;
using ll = long long;

void solve() {
    ll n, m, d;
    cin >> n >> m >> d;

    ll cap = d/ m+1;
    ll ans = (n + cap - 1) / cap;   
    cout << ans << "\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) solve();
    return 0;
}

