using namespace std;
using ll = long long;

void solve() {
    int n; cin >> n;
    int count=0;
    for (int i=0; i<(n/2+1);i++){
        int j=n-2*i;
        if (j%4==0){
            count++;
        }
    }
    cout << count << "\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) solve();
    return 0;
}